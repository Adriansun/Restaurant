            <h2>
                    <a name="timeToCook">Prep:</a> 15 min. Bake: 55 min <strong>Yield:</strong> 24 
            </h2>
            <br/>
            <hr>
            
            <h2>
                    <a name="Ingredients">Ingredients</a>
            </h2>
            
            <ul>
                <li><span title="flour baby">4 cups all-purpose flour</span></li>
                <li><span title="sugar baby">2 cups sugar, divided</span></li>
                <li><span title="give me your soda baby">2 teaspoons baking soda</span></li>
                <li><span title="my eggy baby">2 eggs</span></li>
                <li><span title="buttermilk baby">2 cups buttermilk</span></li>
                <li><span title="canola baby">1/2 cup canola oil</span></li>
                <li><span title="raisin baby">1/2 cup raisins</span></li>
                <li><span title="Tone it down baby">3 teaspoons ground Tone's® Ground Cinnamon</span></li>  
            </ul> 
            
            <hr>
            <h2>
                <a name="Directions">Directions</a>
            </h2>
            
            <ol>
                <li>In a large bowl, combine the flour, 1-1/2 cups sugar, soda and salt. In a small bowl, whisk<br/>
                     the eggs, buttermilk and oil. Stir into dry ingredients just until moistened. Fold in raisins.<br/>
                      Combine cinnamon and remaining sugar; set aside.
                </li>
                
                <li>Spoon half of the batter into two greased 8-in. x 4-in. loaf pans. Sprinkle with half of the<br/>
                     reserved cinnamon-sugar; repeat layers. Cut through batter with a knife to swirl.
                </li>
                
                <li>
                    Bake at 350° for 55-60 minutes or until a toothpick inserted near the center comes out clean.<br/>
                     Cool in pans for 10 minutes before removing from pans to wire racks. Yield: 2 loaves <br/>
                     (12 slices each).
                </li>
            </ol> 
            
            <hr>
            <h2>
                Nutritional Facts
            </h2>
            
            1 slice equals 204 calories, 5 g fat (1 g saturated fat),<br/>
             18 mg cholesterol, 231 mg sodium, 36 g carbohydrate, <br/>
             1 g fiber, 3 g protein. 
            
