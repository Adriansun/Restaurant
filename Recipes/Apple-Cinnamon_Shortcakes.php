			<h2>
					<a name="timeToCook">Prep:</a> 30 min. Bake: 20 min. <strong>Yield:</strong> 8 
			</h2>
			<br/>
			<hr>
			
			<h2>
					<a name="Ingredients">Ingredients</a>
			</h2>
			
			<ul>
				<li><span title="flour baby">2 cups all-purpose flour</span></li>
				<li><span title="sugar baby">2 tablespoons plus 2 teaspoons sugar, divided</span></li>
				<li><span title="give me your powder baby">3 teaspoons baking powder</span></li>
				<li><span title="my cinnamon baby">1 teaspoon ground cinnamon</span></li>
				<li><span title="salty baby">1/2 teaspoon salt</span></li>
				<li><span title="creamy baby">1 cup heavy whipping cream</span></li>
				<li><span title="butter baby">1/2 cup plus 4 teaspoons melted butter, divided</span></li>
				<li><span title="Filling baby">FILLING:</span></li>
				<li><span title="butter baby">1/4 cup butter, cubed</span></li>
				<li><span title="Apple tart baby">4 large tart Apples, peeled and sliced</span></li>
				<li><span title="brown sugar baby - can I have some?!">1/2 cup packed brown sugar</span></li>
				<li><span title="ground your cinnamon, baby">1/2 teaspoon ground cinnamon</span></li>
				<li><span title="nutmeg - because it's good for you, baby">1/8 teaspoon ground nutmeg</span></li>	
			</ul> 
			
			<hr>
			<h2>
				<a name="Directions">Directions</a>
			</h2>
			
			<ol>
				<li>In a large bowl, combine the flour, 2 tablespoons sugar, baking powder, cinnamon <br/>
					 and salt. In a small bowl, combine cream and 1/2 cup butter; stir into dry ingredients <br/>
					 just until moistened.
				</li>
				
				<li>Drop dough by 1/4 cupfuls 2 in. apart onto a greased baking sheet. Brush with remaining <br/>
					butter; sprinkle with remaining sugar. Bake at 375° for 18-20 minutes or until golden <br/>
					brown. Cool slightly.
				</li>
				
				<li>
					For filling, in a large skillet, melt butter. Add apples; saute until tender. Stir in the<br/>
					 brown sugar, cinnamon and nutmeg. Gently split warm shortcakes in half horizontally. <br/>
					 Top bottom halves with apple mixture; replace tops. Serve immediately. Yield: 8 servings.
				</li>
			</ol> 
			
			<hr>
			<h2>
				Nutritional Facts
			</h2>
			
			1 shortcake with 1/3 cup filling equals <br/>
			496 calories, 30 g fat (19 g saturated fat),<br/>
			 91 mg cholesterol, 449 mg sodium, <br/>
			 54 g carbohydrate, 2 g fiber, 4 g protein.
			
